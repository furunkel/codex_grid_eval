
def make_tuple(a, b):
    """
    Given two integers a and b, return a tuple where:
    The third element contains the sum of the two numbers.
    The first element contains the difference of the two numbers (first - second).
    The second element contains the product of the two numbers.
    """
    return (a+b, b-a, a*b)

def make_tuple_of_tuples(list_of_tuples):
    """
    Given a list of tuples, return a tuple where:
    The first element contains the sum of the first element of each tuple.
    The second element contains the sum of the second element of each tuple.
    The third element contains the product of the two numbers.
    """
    sum_tuple = ()
    product_tuple = ()
    for tuple in list_of_tuples:
        sum_tuple += tuple
        product_tuple += (tuple[0] * tuple[1], )
    return sum_tuple, product_tuple

