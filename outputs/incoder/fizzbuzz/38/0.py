
def fizzbuzz(num):
    """
    Given a number, this function returns
        "Fizz" for multiples of five (instead of the number),
        "Buzz" for multiples of twenty-three (instead of the number),
        "FizzBuzz" for multiples of both five and twenty-three  (instead of the number),
        the number itself otherwise.
    """
    if num % 5 == 0:
        return "FizzBuzz"
    elif num % 3 == 0:
        return "Fizz"
    elif num % 5 == 0 and num % 3 == 0:
        return "Buzz"
    else:
        return num


