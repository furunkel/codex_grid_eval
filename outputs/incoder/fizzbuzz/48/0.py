
def fizzbuzz(num):
    """
    Given a number, this function returns
        "Fizz" for multiples of seventeen (instead of the number),
        "Buzz" for multiples of 5 (instead of the number),
        "FizzBuzz" for multiples of both seventeen and 5  (instead of the number),
        the number itself otherwise.
    """
    if num % 15 == 0:
        return "FizzBuzz"
    elif num % 5 == 0:
        return "Buzz"
    elif num % 7 == 0:
        return "Fizz"
    else:
        return num


