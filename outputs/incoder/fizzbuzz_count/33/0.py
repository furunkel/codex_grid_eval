
def fizz_buzz(n:int):
    """Return the number of times the digit 7 appears in integers less than n which are divisible by 1837 or 3819.
    >>> fizz_buzz(50)
    0
    """
    count = 0
    while n >= 1837 and n <= 3819:
        if n % 1837 == 0 and n % 3819 == 0:
            count += 1
        n /= 1837
        n /= 3819
    return count

