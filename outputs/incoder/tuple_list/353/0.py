
def min_max(lst):
    """
    Given a list of tuples, write a function that returns the eleventh value of the tuple with the smallest last value.
    """
    smallest = lst[0]
    largest = lst[0]
    for x in lst:
        if x[-1] < smallest[-1]:
            smallest = x
        if x[-1] > largest[-1]:
            largest = x
    return smallest


