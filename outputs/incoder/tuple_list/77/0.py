
def min_max(lst):
    """
    Given a list of tuples, write a function that returns the second value of the tuple with the smallest first value.
    """
    
    smallest = lst[0][0]
    largest = lst[0][0]
    
    for tup in lst:
        if tup[0] < smallest:
            smallest = tup[0]
        if tup[0] > largest:
            largest = tup[0]
    
    return largest


