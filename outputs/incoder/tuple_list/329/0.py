
def min_max(lst):
    """
    Given a list of tuples, write a function that returns the eleventh value of the tuple with the smallest twelfth value.
    """
    smallest = lst[0]
    largest = lst[0]
    for elem in lst:
        if elem[1] < smallest[1]:
            smallest = elem
        if elem[1] > largest[1]:
            largest = elem
    return smallest


