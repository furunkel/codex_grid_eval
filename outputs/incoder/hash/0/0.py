
def string_to_hash(text):
    """
    Given a string 'text', return its md5 hash equivalent string.
    If 'text' is an empty string, return None.
    """
    if text:
        return hashlib.md5(text.encode('utf-8')).hexdigest()
    return None


def string_to_list(text):
    """
    Given a string 'text', return a list of its words.
    If 'text' is an empty string, return an empty list.
    """
    if text:
        return text.split()
    return []


def string_to_set(text):
    """
    Given a string 'text', return a set of its words.
    If 'text' is an empty string, return an empty set.
    """
    if text:
        return set(text.split())
    return set()


def string_to_tuple(text):
    """
    Given a string 'text', return a tuple of its words.
    If 'text' is an empty string, return an empty tuple.
    """
    if text:
        return tuple(text.split())
    return ()


