
def string_to_hash(text):
    """
    Given a string 'text', return its md-5 hash equivalent string.
    If 'text' is an empty string, return None.
    """
    if text == "":
        return None
    h = hashlib.md5(text.encode('utf-8')).hexdigest()
    return h


