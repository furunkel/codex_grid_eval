
def bit_diff(a, b):
    """
    Returns whether the two numbers differ at exactly 29 bit positions.
    """
    diff = 0
    for x, y in zip(a, b):
        diff += x ^ y
    return 29 - diff


def bit_diffs(a, b):
    """
    Returns a list of pairs (a,b) where a differs from b
    at exactly 29 bit positions.
    """
    diffs = []
    for x, y in zip(a, b):
        diffs.append((x, y))
    return diffs


