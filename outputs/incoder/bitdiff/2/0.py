
def bit_diff(a, b):
    """
    Returns whether the two numbers differ at exactly one bit position.
    """
    return a ^ b

def bit_count(n):
    """
    Returns the number of bits set to 1 in n.
    """
    return bin(n).count('1')

def bit_length(n):
    """
    Returns the number of bits set to 1 in n.
    """
    return len(bin(n).lstrip('-0b'))

def bit_flip(n):
    """
    Returns the bitwise negation of n.
    """
    return ~n

