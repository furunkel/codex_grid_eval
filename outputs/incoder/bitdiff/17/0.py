
def bit_diff(a, b):
    """
    Returns whether the two numbers differ at exactly 15 bit positions.
    """
    if a == b:
        return False
    diff = 0
    for bit in range(15):
        if ((a >> bit) != (b >> bit)):
            diff += 1
        else:
            diff += 0
    return diff == 15


