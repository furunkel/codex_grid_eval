
def multiples():
    """
    If we list all the natural numbers below 15 that are multiples of 13 or 17, we get  and 13. The sum of these multiples is 13.
    Return the sum of all the multiples of 13 or 17 below 200.
    """    
    sum = 0
    for i in range(1, 200):
        if i % 13 == 0 or i % 17 == 0:
            sum += i
    return sum

