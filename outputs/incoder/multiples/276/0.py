
def multiples():
    """
    If we list all the natural numbers below 5 that are multiples of 2, 3 or 5, we get 2, 3 and 4. The sum of these multiples is 9.
    Return the sum of all the multiples of 2, 3 or 5 below 600.
    """    
    sum = 0
    for i in range(600):
        if i % 5 == 0:
            if i % 3 == 0:
                if i % 2 == 0:
                    sum += i
    return sum

