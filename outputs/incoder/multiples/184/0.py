
def multiples():
    """
    If we list all the natural numbers below 15 that are multiples of 13 or 17, we get  and 13. The sum of these multiples is 13.
    Return the sum of all the multiples of 13 or 17 below 124.
    """    
    
    sum = 0
    for i in range(1, 125):
        if i % 13 == 0 or i % 17 == 0:
            sum += i
    return sum

def fibonacci():
    """
    We list the Fibonacci numbers and their sum in the same order. 
    Return the sum of all the numbers in the Fibonacci sequence below 1000.
    """
    
    sum = 0
    a, b = 0, 1
    for i in range(0, 1000):
        sum += a
        a, b = b, a + b
    return sum

