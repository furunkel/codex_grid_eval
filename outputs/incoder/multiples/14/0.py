
def multiples():
    """
    If we list all the natural numbers below 10 that are multiples of 5 or 3, we get 3, 5, 6 and 9. The sum of these multiples is 23.
    Return the sum of all the multiples of 5 or 3 below 100.
    """    
    sum = 0
    for i in range(1, 101):
        if i % 5 == 0 or i % 3 == 0:
            sum += i
    return sum

