def fibonacci():
    """
    Using math sequence, find the sum of the first 20 fibonacci numbers. 
    """
    sum = 0
    for i in range(1, 21):
        sum = sum + i
    return sum

# Problem 5
def fibonacci2():
    """
    Using math sequence, find the sum of the first 20 fibonacci numbers. 
    """
    sum = 0
    a, b = 0, 1
    for i in range(1, 21):
        sum = sum + i
        a, b = b, a + b
    return sum

