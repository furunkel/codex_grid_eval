
def multiples():
    """
    If we list all the natural numbers below 5 that are multiples of 3 or 5, we get  and 3. The sum of these multiples is 3.
    Return the sum of all the multiples of 3 or 5 below 124.
    """    
    sum = 0
    for i in range(1, 125):
        if i % 3 == 0 or i % 5 == 0:
            sum = sum + i
    return sum

