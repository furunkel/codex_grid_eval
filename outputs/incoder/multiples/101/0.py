
def multiples():
    """
    If we list all the natural numbers below 10 that are multiples of 2 or 9, we get 2, 4, 6, 8 and 9. The product of these multiples is 3456.
    Return the product of all the multiples of 2 or 9 below 124.
    """    
    
    multiples = []
    for i in range(1, 125, 2):
        if i % 2 == 0:
            multiples.append(i)
        if i % 9 == 0:
            multiples.append(i)
    return multiples[::-1]

