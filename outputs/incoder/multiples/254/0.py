
def multiples():
    """
    If we list all the natural numbers below 5 that are multiples of 2 or 5, we get 2 and 4. The sum of these multiples is 6.
    Return the sum of all the multiples of 2 or 5 below 124.
    """    
    sum = 0
    for i in range(1, 125):
        if i % 2 == 0 or i % 5 == 0:
            sum += i
    return sum

