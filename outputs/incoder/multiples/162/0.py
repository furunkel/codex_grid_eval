
def multiples():
    """
    If we list all the natural numbers below 15 that are multiples of 2, 3 or 5, we get 2, 3, 4, 5, 6, 8, 9, 10, 12 and 14. The sum of these multiples is 73.
    Return the sum of all the multiples of 2, 3 or 5 below 500.
    """    
    
    sum = 0
    for i in range(500):
        if i % 2 == 0:
            sum += i
        elif i % 3 == 0:
            sum += i
        elif i % 5 == 0:
            sum += i
    return sum

def fibonacci():
    """
    Using two variables, a and b, we can compute and store the sum of all even Fibonacci numbers below n.
    """
    
    a, b = 0, 1
    sum = 0
    for i in range(500):
        sum += a
        a, b = b, a + b
    return sum

