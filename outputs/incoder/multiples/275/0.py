
def multiples():
    """
    If we list all the natural numbers below 5 that are multiples of 2, 3 or 5, we get 2, 3 and 4. The product of these multiples is 24.
    Return the product of all the multiples of 2, 3 or 5 below 500.
    """    
    
    multiples = []
    
    for i in range(500):
        if i % 2 == 0:
            multiples.append(i)
        if i % 3 == 0:
            multiples.append(i)
        if i % 5 == 0:
            multiples.append(i)
    
    return sum(multiples)

