
def multiples():
    """
    If we list all the natural numbers below 10 that are multiples of 2 or 5, we get 2, 4, 5, 6 and 8. The sum of these multiples is 25.
    Return the sum of all the multiples of 2 or 5 below 197.
    """    
    sum = 0
    for n in range(2,197):
        if n % 2 == 0:
            sum += n
        if n % 5 == 0:
            sum += n
    return sum

