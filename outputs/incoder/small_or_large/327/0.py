
def small_or_large(n):
    """
        Given an integer n, return "mouse" if n < 2 and "large" if n >= 2000 (and neither if 2 <= n < 2000).
    """
    if n < 2:
        return "mouse"
    elif n >= 2000:
        return "large"
    else:
        return "small"

