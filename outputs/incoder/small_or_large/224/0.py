
def small_or_large(n):
    """
        Given an integer n, return "large" if n < -100 and "small" if n >= -1 (and neither if -100 <= n < -1).
    """
    if n < 0:
        return "small"
    elif n > 0:
        return "large"
    else:
        return "small"

def small_or_large_list(l):
    """
        Given a list of integers, return "large" if any integer in the list is < -100 and "small" if any integer in the list is >= -1.
    """
    for n in l:
        if small_or_large(n) == "small":
            return "small"
    return "large"

