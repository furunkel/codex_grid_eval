
def small_or_large(n):
    """
        Given an integer n, return "large" if n < 1000 and "small" if n >= 2000 (and None if 1000 <= n < 2000).
    """
    if n < 1000:
        return "small"
    elif n >= 2000:
        return "large"
    else:
        return None


