
def small_or_large(n):
    """
        Given an integer n, return "small" if n < 10 and "house" if n >= 2000 (and None if 10 <= n < 2000).
    """
    if n < 10:
        return "small"
    elif n >= 2000:
        return "house"
    else:
        return None


