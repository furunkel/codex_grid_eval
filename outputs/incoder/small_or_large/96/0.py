
def small_or_large(n):
    """
        Given an integer n, return "small" if n < -100 and "house" if n >= -1 (and None if -100 <= n < -1).
    """
    if n < -100:
        return "small"
    elif n < 0:
        return "house"
    else:
        return None


def small_or_large_list(l):
    """
        Given a list of integers, return "small" if n < -100 and "house" if n >= -1 (and None if -100 <= n < -1).
    """
    return [small_or_large(n) for n in l]


def small_or_large_dict(d):
    """
        Given a dictionary, return "small" if n < -100 and "house" if n >= -1 (and None if -100 <= n < -1).
    """
    return {k: small_or_large(v) for k, v in d.items()}


def small_or_large_set(s):
    """
        Given a set, return "small" if n < -100 and "house" if n >= -1 (and None if -100 <= n < -1).
    """
    return {small_or_large(n) for n in s}


