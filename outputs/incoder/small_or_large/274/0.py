
def small_or_large(n):
    """
        Given an integer n, return "large" if n < -100 and "house" if n >= 15 (and neither if -100 <= n < 15).
    """
    if n < 0:
        return "small"
    elif n >= 15:
        return "large"
    else:
        return "house"


