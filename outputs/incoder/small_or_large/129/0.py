
def small_or_large(n):
    """
        Given an integer n, return "small" if n < -100 and "house" if n >= 2 (and neither if -100 <= n < 2).
    """
    if n < -100:
        return "small"
    elif n >= 2:
        return "house"
    else:
        return "small"

