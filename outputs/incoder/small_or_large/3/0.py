
def small_or_large(n):
    """
        Given an integer n, return "small" if n < -100 and "large" if n >= 2000 (and None if -100 <= n < 2000).
    """
    if n < -100:
        return "small"
    elif n >= 2000:
        return "large"
    else:
        return None


