
def small_or_large(n):
    """
        Given an integer n, return "mouse" if n < 2 and "house" if n >= 15 (and neither if 2 <= n < 15).
    """
    if n < 2:
        return "mouse"
    if n >= 15:
        return "house"
    return "none"


