
def small_or_large(n):
    """
        Given an integer n, return "large" if n < -100 and "house" if n >= -1 (and neither if -100 <= n < -1).
    """
    if n < 0:
        return "large"
    elif n > 0:
        return "house"
    else:
        return "small"

# Test the small_or_large function
