
def small_or_large(n):
    """
        Given an integer n, return "large" if n < 10 and "house" if n >= 2000 (and neither if 10 <= n < 2000).
    """
    if n < 10:
        return "small"
    elif n >= 2000:
        return "large"
    else:
        return "house"


