
def small_or_large(n):
    """
        Given an integer n, return "mouse" if n < 10 and "house" if n >= 15 (and neither if 10 <= n < 15).
    """
    if n < 10:
        return "mouse"
    elif n >= 15:
        return "house"
    else:
        return "none"


