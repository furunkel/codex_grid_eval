def count_small_or_large(numbers):
    """
        Given a list of integers, return a dictionary mapping integers to "mouse" or "house".
    """
    small_or_large_dict = {}
    for n in numbers:
        small_or_large_dict[n] = small_or_large(n)
    return small_or_large_dict


