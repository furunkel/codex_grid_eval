
def small_or_large(n):
    """
        Given an integer n, return "small" if n < 2 and "house" if n >= 2000 (and None if 2 <= n < 2000).
    """
    if n < 2:
        return "small"
    elif n >= 2000:
        return "house"
    else:
        return None


