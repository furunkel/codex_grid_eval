
def double_letters(string):
    """
    Given a string, return the string, doubling every consonant (lower-case or upper-case) character, and tripling every number.
    All other characters should be output a single time each.
    """
    double_letters_string = ""
    for letter in string:
        if letter.islower():
            double_letters_string += letter
        elif letter.isupper():
            double_letters_string += letter.upper()
        else:
            double_letters_string += letter
    return double_letters_string


