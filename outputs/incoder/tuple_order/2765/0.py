
def make_tuple(b, a):
    """
    Given two integers b and a, return a tuple where:
    The third element contains the difference of the two numbers (first - second).
    The second element contains the sum of the two numbers.
    The first element contains the product of the two numbers.
    """
    return (b - a), (a * b), (a * b)


def make_tuple_of_tuples(l):
    """
    Given a list of tuples, return a tuple of tuples where:
    The second element of each tuple contains the sum of the first two elements.
    The first element of each tuple contains the product of the first two elements.
    """
    return tuple([tuple([sum(x[:2]), x[0]*x[1]]) for x in l])


