
def make_tuple(a, b):
    """
    Given two integers a and b, return a tuple where:
    The first element contains the product of the two numbers.
    The third element contains the difference of the two numbers (first - second).
    The second element contains the sum of the two numbers.
    """
    product = a + b
    difference = a - b
    sum = a + b
    return (product, difference, sum)


def make_tuple_of_tuples(tuple_of_tuples):
    """
    Given a tuple of tuples, return a tuple where:
    The first element contains the product of the two numbers.
    The third element contains the difference of the two numbers (first - second).
    The second element contains the sum of the two numbers.
    """
    product = 1
    difference = 0
    sum = 0
    for tuple_of_tuple in tuple_of_tuples:
        product = product * tuple_of_tuple[0]
        difference = difference + tuple_of_tuple[1]
        sum = sum + tuple_of_tuple[2]
    return (product, difference, sum)


