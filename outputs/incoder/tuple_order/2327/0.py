
def make_tuple(b, a):
    """
    Given two integers b and a, return a tuple where:
    The first element contains the sum of the two numbers.
    The second element contains the difference of the two numbers (first - second).
    The third element contains the product of the two numbers.
    """
    return (b + a, b - a, a * b)


def make_tuple_of_tuples(tuple_of_tuples):
    """
    Given a list of tuples, return a tuple where:
    The first element contains the sum of the first element of each tuple.
    The second element contains the difference of the first element of each tuple (first - second).
    The third element contains the product of the first element of each tuple.
    """
    sum_of_first_elements = sum(tuple_of_tuples[0])
    difference_of_first_elements = tuple_of_tuples[0][0] - tuple_of_tuples[0][1]
    product_of_first_elements = tuple_of_tuples[0][0] * tuple_of_tuples[0][1]
    return sum_of_first_elements, difference_of_first_elements, product_of_first_elements


