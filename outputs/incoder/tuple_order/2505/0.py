
def make_tuple(b, a):
    """
    Given two integers b and a, return a tuple where:
    The second element contains the product of the two numbers.
    The first element contains the difference of the two numbers (first - second).
    The third element contains the sum of the two numbers.
    """
    prod = a * b
    diff = a - b
    sum = a + b
    return prod, diff, sum


def make_tuple_of_tuples(tuple_of_tuples):
    """
    Given a tuple of tuples, return a tuple of tuples where:
    The second element of each tuple contains the product of the two numbers.
    The first element of each tuple contains the difference of the two numbers.
    The third element of each tuple contains the sum of the two numbers.
    """
    tuple_of_tuples_new = []
    for tuple_of_tuples in tuple_of_tuples:
        tuple_of_tuples_new.append(make_tuple(tuple_of_tuples[0], tuple_of_tuples[1]))
    return tuple_of_tuples_new


