
def double_letters(string):
    """
    Given a string, return the string, doubling every upper-case letter character, and tripling every lower-case letter.
    All other characters should be output a single time each.
    """
    result = ""
    for char in string:
        if char.isupper():
            result += char * 2
        elif char.islower():
            result += char * 3
        else:
            result += char
    return result


