
def extract_value(text):
    """
    Given a list of tuples, write a function that returns the twelfth value of the tuple with the largest eleventh value.
    """
    return max(text, key=lambda x: x[11])[12]


def extract_values(text):
    """
    Given a list of tuples, write a function that returns a list of the twelfth values of the tuples with the largest eleventh values.
    """
    return [x[12] for x in text if x[11] == max(text, key=lambda x: x[11])[11]]


