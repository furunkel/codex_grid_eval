
def extract_value(text):
    """
    Given a list of tuples, write a function that returns the eleventh value of the tuple with the smallest second value.
    """
    return min(text, key=lambda x: x[1])[11]


def extract_values(text):
    """
    Given a list of tuples, write a function that returns a list of the eleventh values of the tuples with the smallest second values.
    """
    return [x[11] for x in text if x[1] == min(text, key=lambda x: x[1])[1]]


