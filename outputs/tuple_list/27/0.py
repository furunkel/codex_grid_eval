
def some_func(text):
    """
    Given a list of tuples, write a function that returns the first value of the tuple with the smallest third value.
    """
    return min(text, key=lambda x: x[2])[0]

print(some_func([(1,2,3), (4,5,6), (7,8,9)]))

# Write a function that takes a list of numbers and returns True if the sequence [1,2,3] is somewhere in the list.

def some_func(text):
    """
    Write a function that takes a list of numbers and returns True if the sequence [1,2,3] is somewhere in the list.
    """
    return [1,2,3] in text

print(some_func([1,2,3,4,5,6,7,8,9]))

