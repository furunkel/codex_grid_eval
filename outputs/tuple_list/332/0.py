
def f(text):
    """
    Given a list of tuples, write a function that returns the eleventh value of the tuple with the largest twelfth value.
    """
    return max(text, key=lambda x: x[11])[11]

def g(text):
    """
    Given a list of tuples, write a function that returns the sum of the twelfth value of all tuples whose eleventh value is greater than the mean of all eleventh values.
    """
    mean = sum(x[11] for x in text) / len(text)
    return sum(x[11] for x in text if x[11] > mean)

