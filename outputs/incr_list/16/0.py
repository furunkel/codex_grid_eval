
def decr_list(l: list):
    """Return list with elements decremented by 2.
    >>> decr_list([1, 2, 3])
    [-1, 0, 1]
    """
    return [x - 2 for x in l]

def decr_list_map(l: list):
    """Return list with elements decremented by 2.
    >>> decr_list_map([1, 2, 3])
    [-1, 0, 1]
    """
    return list(map(lambda x: x - 2, l))

