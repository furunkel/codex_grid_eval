
def incr_list(l: list):
    """Return list with elements incremented by 1.
    >>> incr_list([1, 2, 3])
    [2, 3, 4]
    """
    return [x + 1 for x in l]

def incr_list_map(l: list):
    """Return list with elements incremented by 1.
    >>> incr_list_map([1, 2, 3])
    [2, 3, 4]
    """
    return list(map(lambda x: x + 1, l))

