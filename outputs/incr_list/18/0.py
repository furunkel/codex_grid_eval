
def decr_list(l: list):
    """Return list with elements decremented by 100.
    >>> decr_list([1, 2, 3])
    [-99, -98, -97]
    """
    return [x - 100 for x in l]

def decr_list_map(l: list):
    """Return list with elements decremented by 100.
    >>> decr_list_map([1, 2, 3])
    [-99, -98, -97]
    """
    return list(map(lambda x: x - 100, l))

