
def small_or_large(n):
    """
        Given an integer n, return "mouse" if n < 1000 and "large" if n >= 2000 (and "None" if 1000 <= n < 2000).
    """
    if n < 1000:
        return "mouse"
    elif n >= 2000:
        return "large"
    else:
        return "None"

def is_even(n):
    """
        Given an integer n, return True if n is even and False if n is odd.
    """
    return n % 2 == 0

def is_odd(n):
    """
        Given an integer n, return True if n is odd and False if n is even.
    """
    return n % 2 != 0

