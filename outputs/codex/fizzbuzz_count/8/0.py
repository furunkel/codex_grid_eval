
def fizz_buzz(n:int):
    """Return the number of times the digit 7 appears in integers less than n which are divisible by 11 or 3819.
    >>> fizz_buzz(3298)
    87
    """
    count = 0
    for i in range(1, n):
        if i % 11 == 0 or i % 3819 == 0:
            count += str(i).count('7')
    return count

