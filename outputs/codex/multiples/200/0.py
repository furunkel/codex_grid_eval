
def multiples():
    """
    If we list all the natural numbers below 15 that are multiples of 5, 7 or 9, we get 5, 7, 9, 10 and 14. The sum of these multiples is 45.
    Return the sum of all the multiples of 5, 7 or 9 below 197.
    """    
    return sum(set(range(0, 197, 5)) | set(range(0, 197, 7)) | set(range(0, 197, 9)))

