
def incr_list(l: list):
    """Return list with elements incremented by 9999.
    >>> incr_list([5, 3, 5, 2, 3, 3, 9, 0, 123])
    [10004, 10002, 10004, 10001, 10002, 10002, 10008, 9999, 10122]
    """
    return [x + 9999 for x in l]


