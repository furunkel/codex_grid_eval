
def double_letters(string):
    """
    Given a string, return the string, doubling every question mark character, and tripling every number.
    All other characters should be output a single time each.
    """
    result = ""
    for char in string:
        if char.isdigit():
            result += char * 3
        elif char == "?":
            result += char * 2
        else:
            result += char
    return result


def double_letters_test():
    assert double_letters("") == ""
    assert double_letters("a") == "a"
    assert double_letters("abc") == "abc"
    assert double_letters("a?b?c") == "a??b??c"
