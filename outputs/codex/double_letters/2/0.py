
def double_letters(string):
    """
    Given a string, return the string, doubling every letter character, and tripling every vowel (lower-case or upper-case).
    All other characters should be output a single time each.
    """
    vowels = "aeiouAEIOU"
    new_string = ""
    for char in string:
        if char in vowels:
            new_string += char * 3
        else:
            new_string += char * 2
    return new_string

