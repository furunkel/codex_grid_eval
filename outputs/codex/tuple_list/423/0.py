
def some_func(lst):
    """
    Given a list of tuples, write a function that returns the last value of the tuple with the smallest last value.
    """
    return min(lst, key=lambda x: x[-1])[-1]

print(some_func([(1, 2), (4, 1), (9, 10), (13, -3)]))

# Output: -3

