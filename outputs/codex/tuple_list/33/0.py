
def some_func(lst):
    """
    Given a list of tuples, write a function that returns the first value of the tuple with the largest third value.
    """
    return max(lst, key=lambda x: x[2])[0]

print(some_func([(1,2,3), (4,5,6), (7,8,9)]))

# Output: 7

