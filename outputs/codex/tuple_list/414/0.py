
def extract_value(lst):
    """
    Given a list of tuples, write a function that returns the last value of the tuple with the largest eleventh value.
    """
    return max(lst, key=lambda x: x[11])[-1]


def extract_values(lst):
    """
    Given a list of tuples, write a function that returns a list of the eleventh values.
    """
    return [x[11] for x in lst]


