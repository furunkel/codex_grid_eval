
def small_or_large(n):
    """
        Given an integer n, return "mouse" if n < 2 and "large" if n >= 15 (and "neither" if 2 <= n < 15).
    """
    if n < 2:
        return "mouse"
    elif n >= 15:
        return "large"
    else:
        return "neither"

def is_even(n):
    """
        Given an integer n, return True if n is even and False if n is odd.
    """
    return n % 2 == 0

def is_odd(n):
    """
        Given an integer n, return True if n is odd and False if n is even.
    """
    return n % 2 == 1

