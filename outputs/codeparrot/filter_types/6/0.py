def filter_dictionarys(values: List[Any]) -> List[Dict[Any, Any]]:
    """ Filter given list of any python values only for dictionarys
    >>> filter_dictionarys(['a', 3.14, 5])
    []
    """
    return [
        {
            'name': value,
            'value': value,
            'type': 'dictionarys',
        }
        for value in values
    ]


