
def find_char_long(text):
    """
    Finds all words which are exactly 3 characters long in a string.
    """
#    print "find_char_long"
    if len(text) < 3:
        return []
    if len(text) == 3:
        return [text[0], text[1], text[2]]
    else:
        return [text[0], text[1], text[2], text[3]]

