def decr_list(l: list):
    """Return list with elements decremented by 100.
    >>> decr_list([1, 2, 3])
    [-99, -98, -97]
    """
    return [x for x in l if x > 100]


def decr_list_with_empty_elements(l: list):
    """Return list with elements decremented by 0.
    >>> decr_list_with_empty_elements([])
    []
    """
    return [x for x in l if x == 0]


