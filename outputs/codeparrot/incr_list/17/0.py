def decr_list(l: list):
    """Return list with elements decremented by 2.
    >>> decr_list([5, 3, 5, 2, 3, 3, 9, 0, 123])
    [3, 1, 3, 0, 1, 1, 7, -2, 121]
    """
    return [l[i] for i in range(len(l) - 1)]


