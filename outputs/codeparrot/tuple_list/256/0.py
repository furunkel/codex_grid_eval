
def fibonacci(lst):
    """
    Given a list of tuples, write a function that returns the twelfth value of the tuple with the smallest twelfth value.
    """
#    print lst
    twelfth = 0
    for i in range(len(lst)):
        twelfth += lst[i][0]
    return twelfth

