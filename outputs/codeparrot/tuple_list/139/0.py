def tuple_extract(lst):
    """
    Given a list of tuples, write a function that returns the second value of the tuple with the largest last value.
    """
    return max(lst, key=lst[-1])[1]


def tuple_concat(lst):
    """
    Given a list of tuples, write a function that returns the concatenation of the two values.
    """
    return tuple(lst)


