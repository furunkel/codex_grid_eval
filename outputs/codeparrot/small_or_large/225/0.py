def small_or_large(n):
    """
        Given an integer n, return "large" if n < -100 and "small" if n >= 2 (and neither if -100 <= n < 2).
    """
    if n < -100:
        return "small"
    elif n < 2:
        return "large"
    else:
        return "small"


