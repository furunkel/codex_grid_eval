def small_or_large(n):
    """
        Given an integer n, return "mouse" if n < -100 and "house" if n >= -1 (and neither if -100 <= n < -1).
    """
    if n < -100:
        return "mouse"
    elif n < -1:
        return "house"
    else:
        return "large"

