def small_or_large(n):
    """
        Given an integer n, return "mouse" if n < -100 and "house" if n >= 2 (and None if -100 <= n < 2).
    """
    if n < -100:
        return "mouse"
    elif n < 2:
        return "house"
    else:
        return None


def get_random_color(n):
    """
        Given an integer n, return a random color.
    """
    if n < 0:
        return "black"
    elif n < 1:
        return "white"
    else:
        return "black"


