def small_or_large(n):
    """
        Given an integer n, return "mouse" if n < 2 and "large" if n >= 15 (and None if 2 <= n < 15).
    """
    if n < 2:
        return "mouse"
    elif n < 15:
        return "large"
    else:
        return None


