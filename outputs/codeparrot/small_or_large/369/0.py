def small_or_large(n):
    """
        Given an integer n, return "mouse" if n < -100 and "small" if n >= 2 (and neither if -100 <= n < 2).
    """
    if n < -100:
        return "mouse"
    elif n < 2:
        return "small"
    else:
        return "large"


