def small_or_large(n):
    """
        Given an integer n, return "mouse" if n < 10 and "large" if n >= 2000 (and neither if 10 <= n < 2000).
    """
    if n < 10:
        return "mouse"
    elif n < 2000:
        return "large"
    else:
        return "small"


