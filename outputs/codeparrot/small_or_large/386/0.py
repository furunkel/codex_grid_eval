def small_or_large(n):
    """
        Given an integer n, return "mouse" if n < -100 and "house" if n >= 15 (and None if -100 <= n < 15).
    """
    if n < -100:
        return "mouse"
    elif n < 15:
        return "house"
    else:
        return None

