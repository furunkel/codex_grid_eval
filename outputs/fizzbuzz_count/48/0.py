
def fizz_buzz(n:int):
    """Return the number of times the digit 4 appears in integers less than n which are divisible by 13 or 11.
    >>> fizz_buzz(50)
    2
    """
    count = 0
    for i in range(n):
        if i % 13 == 0 or i % 11 == 0:
            count += str(i).count('4')
    return count

